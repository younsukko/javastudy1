
public class day2two {

	public static void main(String[] args) {
		int a = 6, b = 4;
		double c = 4.0;
		
		System.out.println(a + "+" + b + "=" + (a + b));
		System.out.println(a + "-" + b + "=" + (a - b));
		System.out.println(a + "*" + b + "=" + a * b);
		System.out.println(a + "/" + b + "=" + a / b);	
		
		System.out.println(a + " / " + c + " = " + (a / c));
		

	}

}
